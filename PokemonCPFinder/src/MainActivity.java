	static int getCP(double cpm, int hp, int hpIV, int atk, int atkIV, int def, int defIV) {
		return (int) Math.floor(Math.sqrt(hp + hpIV) * (atk + atkIV) * Math.sqrt(def + defIV) * cpm * cpm / 10);
	}
	
